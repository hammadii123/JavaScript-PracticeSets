// 3. Naming Rules
document.write("<h1>Rules for Naming JavaScript Variables</h1>");
document.write("a) When naming variables, only letters, digits, dollar signs ($), and underscores (_) are allowed.<br>");
document.write("For example: <strong>$total_1st_Amount</strong> is valid.<br><br>");

document.write("b) Variable names should always begin with a letter, dollar sign ($), or underscore (_).<br>");
document.write("Examples: <strong>$name</strong>, <strong>_userID</strong>, or <strong>userScore</strong>.<br><br>");

document.write("c) JavaScript variables distinguish between uppercase and lowercase characters.<br>");
document.write("For instance, <strong>myVariable</strong> and <strong>myvariable</strong> are treated as two distinct variables.<br><br>");

document.write("d) Avoid using JavaScript reserved words or keywords like <strong>class</strong>, <strong>return</strong>, or <strong>function</strong> when naming your variables.<br>");
